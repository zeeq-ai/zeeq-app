// Use a custom nanoid generator with a fixed alphabet and length to ensure the
// generated cart name is URL-safe and consistent in length.
const alphaNumericNanoId = customAlphabet(alphanumeric, 10);

/**
 * Generates a human-memorable cart name: adjective-noun-nanoid10, e.g.
 * "snappy-lake-a1b2c3d4e5". Called once, client-side, when a draft cart is
 * created; the result is never regenerated or editable.
 */
export function generateCartName(): string {
  const adjective = pickRandom(ADJECTIVES);
  const noun = pickRandom(NOUNS);

  return `${adjective}-${noun}-${alphaNumericNanoId()}`;
}

function pickRandom<T>(items: readonly T[]): T {
  return items[Math.floor(Math.random() * items.length)]!;
}
